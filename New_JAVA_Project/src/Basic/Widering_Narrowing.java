package Basic;

public class Widering_Narrowing {
	
	public static void main(String[] args) {
		
		int x=(int)5.5;  //narrowing
		System.out.println(x);
		
		double d=(double)50;  //widering 
		System.out.println(d);
		
		
		long l=(long)130;
		System.out.println(l);
		
		
		byte b=(byte)212324389020948l;
		System.out.println(b);
		
	}

}
